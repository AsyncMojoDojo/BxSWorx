					{
						if ( ! this._UCCreated )
							{
								this._UCButton	= new	UC_MenuButton
									{
											Dock									=	this.DockStyle
										,	TabIndex							=	this.TabIndex
										,	Name									=	this.ID
										, SetFocusColour				= this.FocusIndicatorColour
