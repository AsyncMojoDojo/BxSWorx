				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public UI_Host(  )
					{
						this.CurrentUIs				= new	ConcurrentDictionary<string, IController_Base>();
						this._Locks						= new Dictionary<string, object>();
						this._SlimLock				=	new	SemaphoreSlim(1,1);
						this._IsShuttingDown	= 0;
					}

			#endregion

			//===========================================================================================
			#region "Declarations"

				private	const	string	UI_SAPSessions	=	"SAPSessions";
				//.................................................
				public	event	Action	ShutdownEvent;

			#endregion

			//===========================================================================================
			#region "Properties"

				internal	string	UITag_SAPSessions		{ get	=> UI_SAPSessions; }

			#endregion

			//===========================================================================================
			#region "Methods: Exposed"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public void	ShowUI( string ID )
					{
						if ( this._IsShuttingDown.Equals(1) )		return;
						//...............................................
						if ( ! this.CurrentUIs.TryGetValue( ID , out IController_Base lo_MvC ) )
							{
								switch ( ID )
									{
										case UI_SAPSessions:	{	this.Add_SAPSessionsUI();		break;	}
										//...
										default:	break;
									}
								//... Try again
								if ( ! this.CurrentUIs.TryGetValue( ID , out lo_MvC ) )		return;
							}
						//...............................................
						lo_MvC.ToggleView();
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public void ShutdownUI()
					{
						if ( this._IsShuttingDown.Equals(1) )		return;
						this._SlimLock.Wait();
						if ( this._IsShuttingDown.Equals(1) )		return;
						Interlocked.CompareExchange( ref this._IsShuttingDown , 1 , 0 );
						//...............................................
						if ( this.ShutdownEvent != null)
							{	ShutdownEvent();	}

						this.CurrentUIs.Clear();
						//...............................................
						this._SlimLock.Release();
					}

			#endregion

			//===========================================================================================
			#region "Methods: Private"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				// *** SAP BDC SESSION ***
				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private	bool Add_SAPSessionsUI()
					{
						return	this.Add_UI( new SAPBDC_Controller( this.UITag_SAPSessions ) );
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private	bool Add_UI( IController_Base mvc )
					{
						bool	lb_Ret	= false;
						//...
						if ( this.CurrentUIs.TryAdd( mvc.ID , mvc ) )
							{
								mvc.FormClosed	+=	this.OnFormClosed;
								this.ShutdownEvent	+=	mvc.Shutdown;
								lb_Ret					 =	true;
							}
						//...
						return	lb_Ret;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private	bool Remove_UI( string	id )
					{
						return	this.CurrentUIs.TryRemove( id , out IController_Base x );
					}

			#endregion

			//===========================================================================================
			#region "Event Handlers: Private"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private	void OnFormClosed( object sender , EventArgs e )
					{
						this.Remove_UI( ((IController_Base) sender ).ID );
					}

			#endregion

