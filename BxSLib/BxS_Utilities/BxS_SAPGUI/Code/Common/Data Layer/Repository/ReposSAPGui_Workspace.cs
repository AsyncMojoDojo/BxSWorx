					{
						return	this._DC.Workspaces.Create(ID);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOWorkspace GetWorkspace(Guid ID)
					{
						return	this._DC.Workspaces.Get(ID);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool	AddUpdateWorkspace(	Guid		ID					,
																				string	Description		)
					{
						IDTOWorkspace lo_DTO = this.CreateWorkspaceDTO(ID);
						//.............................................
						lo_DTO.Description	= Description;
						//.............................................
						return	this.AddUpdateWorkspace(lo_DTO);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateWorkspace(IDTOWorkspace DTO)
					{
						return	this._DC.Workspaces.AddUpdate(DTO.UUID, DTO);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveWorkspace(Guid ID)
					{
						IList<Guid> lt_Items	= this._DC.Items.KeyListFor("WSID", ID);
						IList<Guid> lt_Nodes	= this._DC.Nodes.KeyListFor("WSID", ID);
						//.............................................
						if (		this._DC.Items.Remove(lt_Items).Equals(lt_Items.Count)
								 && this._DC.Nodes.Remove(lt_Nodes).Equals(lt_Nodes.Count)	)
							{
								return	this._DC.Workspaces.Remove(ID);
							}
						else
							{	return	false; }
					}

			#endregion

		}
