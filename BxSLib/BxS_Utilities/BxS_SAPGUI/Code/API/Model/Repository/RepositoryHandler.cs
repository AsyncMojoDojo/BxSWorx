namespace SAPGUI.COM.DL
{
	internal class RepositoryHandler : IRepositoryHandler
		{
			#region "Constructor"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				internal RepositoryHandler(Repository repository)
					{
						this._Repository	= repository;
						//.............................................
						this.IsDirty	= false;
					}

			#endregion

			//===========================================================================================
			#region "Declarations"

				private readonly Repository		_Repository;

			#endregion

			//===========================================================================================
			#region "Proprties"

				internal bool IsDirty { get; private set; }

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: Creators"

				public IDTOMsgServer			CreateMsgServer()			{	return	new DTOMsgServer();			}
				public IDTOService				CreateService()				{ return	new DTOService();				}
				public IDTOWorkspace			CreateWorkspace()			{ return	new DTOWorkspace();			}
				public IDTOWorkspaceNode	CreateWorkspaceNode()	{ return	new DTOWorkspaceNode(); }
				public IDTOWorkspaceItem	CreateWorkspaceItem()	{ return	new DTOWorkspaceItem(); }

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: Workspace"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOWorkspace GetWorkspace(Guid ID)
					{
						IDTOWorkspace	lo_DTO	= this.CreateWorkspace();
						this._Repository.WorkSpaces.TryGetValue(ID, out lo_DTO);
						return	lo_DTO;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateWorkspace(IDTOWorkspace DTO)
					{
						bool lb_Ret	= false;
						//.............................................
						if (this._Repository.WorkSpaces.ContainsKey(DTO.UUID))
							{
								this._Repository.WorkSpaces[DTO.UUID]	= DTO;
								lb_Ret	= true;
							}
						else
							{
								lb_Ret	= this._Repository.WorkSpaces.TryAdd(DTO.UUID, DTO);
							}

						if (lb_Ret)		this.IsDirty	= true;
						//.............................................
						return	lb_Ret;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveWorkspace(Guid ID)
					{
						bool lb_Ret	= false;
						//.............................................
						lb_Ret	= this._Repository.WorkSpaces.Remove(ID);
						if (lb_Ret)	this.IsDirty	= true;
						//.............................................
						return	lb_Ret;
					}

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: Workspace: Node"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateNode(IDTOWorkspaceNode DTO)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveNode(Guid ID)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOWorkspaceNode GetNode(Guid ID)
					{
						return	this.CreateWorkspaceNode();
					}

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: Workspace: Item"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateWSItem(Guid WspaceID, IDTOWorkspaceItem DTO)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateNodeItem(Guid WspaceID, Guid NodeID, IDTOWorkspaceItem DTO)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveWSItem(Guid WspaceID, Guid ID)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveNodeItem(Guid WspaceID, Guid NodeID, Guid ID)
					{
						return	false;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOWorkspaceItem GetWSItem(Guid WspaceID, Guid ID)
					{
						return	this.CreateWorkspaceItem();
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOWorkspaceItem GetNodeItem(Guid WspaceID, Guid NodeID, Guid ID)
					{
						return	this.CreateWorkspaceItem();
					}

			#endregion




			//===========================================================================================
			#region "Methods: Exposed: Service"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOService GetService(Guid ID)
					{
						IDTOService	lo_DTO	= this.CreateService();
						this._Repository.Services.TryGetValue(ID, out lo_DTO);
						return	lo_DTO;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateService(IDTOService DTO)
					{
						bool lb_Ret	= false;
						//.............................................
						if (this._Repository.Services.ContainsKey(DTO.UUID))
							{
								this._Repository.Services[DTO.UUID]	= DTO;
								lb_Ret	= true;
							}
						else
							{
								lb_Ret	= this._Repository.Services.TryAdd(DTO.UUID, DTO);
							}

						if (lb_Ret)		this.IsDirty	= true;
						//.............................................
						return	lb_Ret;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveService(Guid ID)
					{
						bool lb_Ret	= false;
						//.............................................
						if (this.ServiceInUse(ID))
							{
								lb_Ret	= this._Repository.Services.Remove(ID);
								if (lb_Ret)	this.IsDirty	= true;
							}
						//.............................................
						return	lb_Ret;
					}

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: Message Server"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public IDTOMsgServer GetMsgServer(Guid ID)
					{
						IDTOMsgServer	lo_DTO	= this.CreateMsgServer();
						this._Repository.MsgServers.TryGetValue(ID, out lo_DTO);
						return	lo_DTO;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool AddUpdateMsgServer(IDTOMsgServer DTO)
					{
						bool lb_Ret	= false;
						//.............................................
						if (this._Repository.MsgServers.ContainsKey(DTO.UUID))
							{
								this._Repository.MsgServers[DTO.UUID]	= DTO;
								lb_Ret	= true;
							}
						else
							{
								lb_Ret	= this._Repository.MsgServers.TryAdd(DTO.UUID, DTO);
							}

						if (lb_Ret)		this.IsDirty	= true;
						//.............................................
						return	lb_Ret;
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public bool RemoveMsgServer(Guid ID)
					{
						bool lb_Ret	= false;
						//.............................................
						if (this.MsgServerInUse(ID))
							{
								lb_Ret	= this._Repository.MsgServers.Remove(ID);
								if (lb_Ret)	this.IsDirty	= true;
							}
						//.............................................
						return	lb_Ret;
					}

			#endregion

			//===========================================================================================
			#region "Methods: Exposed: General"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public void Clear()
					{
						if (		this._Repository.MsgServers.Count.Equals(0)
								&&	this._Repository.Services.Count.Equals	(0)
								&&	this._Repository.WorkSpaces.Count.Equals(0)	)
							{
								return;
							}
						//.............................................
						this.IsDirty	= true;
						this._Repository.Clear();
					}

			#endregion

			//===========================================================================================
			#region "Methods: Private"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private bool MsgServerInUse(Guid ID)
					{
						return	!this._Repository.Services.Count(kvp => kvp.Value.MSID.Equals(ID)).Equals(0);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				private bool ServiceInUse(Guid ID)
					{
						bool lb_CntItm		= this._Repository.WorkSpaces.SelectMany(
																	ws => ws.Value.Items.Where(
																		it => it.Key.Equals(ID)))
																			.Count().Equals(0);

						bool lb_CntNde		= this._Repository.WorkSpaces.SelectMany(
																	ws => ws.Value.Nodes.SelectMany(
																		nd => nd.Value.Items.Where(
																			it => it.Key.Equals(ID))))
																				.Count().Equals(0);

						return	!lb_CntItm || !lb_CntNde;
				}

			#endregion

		}
}