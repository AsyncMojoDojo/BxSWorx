namespace BxS_SAPGUI.USR
{
	internal class USRController : ControllerSourceBase
		{
			#region "Constructors"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				internal USRController(IRepository repository, string fullPathName, IO FileIO, DCSerializer dcSerializer, bool AutoLoad = true)
									: base(repository)
					{
						this._DCFullName	= fullPathName	;
						this._IO					= FileIO				;
						this._DCSer				= dcSerializer	;
						//.............................................
						if (AutoLoad)		this.LoadDataContainer();
					}

			#endregion

			//===========================================================================================
			#region "Declarations"

				private readonly string	_DCFullName	;
				//.................................................
				private readonly IO							_IO		 ;
				private	readonly DCSerializer		_DCSer ;

			#endregion

			//===========================================================================================
			#region "Methods: Exposed"

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				internal bool FileExists()
					{
						return	this._IO.FileExists(this._DCFullName);
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
				public override void Save(bool forceSave = false)
					{
						if (forceSave || this.Repository.IsDirty)
							{
								this._IO.WriteFile(this._DCFullName, this._DCSer.Serialize(this.Repository.GetDataContainer()));
							}
					}

				//¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨
